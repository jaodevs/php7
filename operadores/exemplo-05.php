<?php
$a = 34;

$b = 35;

var_dump($a <=> $b);

// O resultado da comparação será:
// -1 se $a for menor que $b,
// 0 se $a for igual a $b
// 1 se $a for maior que $b.
// O operador nave espacial é particularmente útil ao classificar arrays ou comparar dois valores sem a necessidade de múltiplas instruções condicionais. Isso simplifica o código e o torna mais conciso.